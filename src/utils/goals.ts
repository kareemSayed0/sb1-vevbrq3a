export function getGoalAdvice(goalType: string, netSalary: number): string {
  const goal = FINANCIAL_GOALS.find(g => g.id === goalType);
  if (!goal) return '';

  const monthlySavings = netSalary * 0.2; // 20% من الراتب للادخار

  return `
${goal.icon} ${goal.label}

💡 نصائح لتحقيق الهدف:
${goal.tips.map(tip => `• ${tip}`).join('\n')}

📊 التحليل المالي:
• راتبك الصافي: ${netSalary.toLocaleString()} ج.م
• المبلغ المقترح للادخار شهرياً: ${monthlySavings.toLocaleString()} ج.م (20% من راتبك)

📝 خطة العمل:
• حدد هدفك بشكل واضح ومفصل
• افتح حساب توفير منفصل للهدف
• اجعل الادخار تلقائياً في بداية كل شهر
• راجع خطتك وتقدمك بشكل دوري
• اضبط الخطة حسب الظروف والمتغيرات

💪 نصائح إضافية:
• ابحث عن طرق لزيادة دخلك
• قلل المصروفات غير الضرورية
• استشر الخبراء في مجال هدفك
• كن مرناً في تعديل خطتك`;
}